#include "3-calc.h"

/**
 * op_add - adds
 * @a: number 1
 * @b: number 2
 * Return: int
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts
 * @a: number 1
 * @b: number 2
 * Return: int
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - multiplies
 * @a: number 1
 * @b: number 2
 * Return: int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides
 * @a: number 1
 * @b: number 2
 * Return: int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo
 * @a: number 1
 * @b: number 2
 * Return: int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
